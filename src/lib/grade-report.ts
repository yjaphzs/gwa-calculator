/**
 * Printable grade documents.
 *
 * Renders the user's own data into a clean, self-contained HTML document and
 * prints it via a hidden iframe (so the browser's "Print / Save as PDF" dialog
 * produces a tidy page, isolated from the app's styles and dark mode).
 *
 * IMPORTANT: these are a personal-reference convenience copy of data the user
 * typed in — NOT official academic records. The disclaimer below is rendered
 * on every document and the wording must stay consistent with the in-app Terms
 * and Conditions and Privacy Policy.
 */
import {
    computeGwa,
    getAcademicHonor,
    getLatinHonor,
    HONOR_MIN_UNITS,
} from "@/lib/academic";
import type { Semester, Subject } from "@/types";

// ── Formatting helpers ───────────────────────────────────────────────────────
function escapeHtml(value: string): string {
    return value
        .replace(/&/g, "&amp;")
        .replace(/</g, "&lt;")
        .replace(/>/g, "&gt;")
        .replace(/"/g, "&quot;")
        .replace(/'/g, "&#39;");
}

function formatGrade(grade: number): string {
    return grade.toFixed(2);
}

function formatUnits(units: number): string {
    return Number.isInteger(units) ? String(units) : units.toFixed(1);
}

function formatGwa(gwa: number | null): string {
    return gwa === null ? "—" : gwa.toFixed(3);
}

function formatToday(): string {
    return new Date().toLocaleDateString(undefined, {
        year: "numeric",
        month: "long",
        day: "numeric",
    });
}

// Order used to sort semesters chronologically (oldest first) in summaries.
const SEMESTER_ORDER: Record<string, number> = { First: 1, Second: 2, Third: 3 };

function sortChronologically(semesters: Semester[]): Semester[] {
    return [...semesters].sort((a, b) => {
        const aYear = parseInt(a.schoolYear.split("-")[0], 10) || 0;
        const bYear = parseInt(b.schoolYear.split("-")[0], 10) || 0;
        if (aYear !== bYear) return aYear - bYear;
        return (SEMESTER_ORDER[a.semester] ?? 0) - (SEMESTER_ORDER[b.semester] ?? 0);
    });
}

// ── HTML fragments ───────────────────────────────────────────────────────────
function subjectsTable(subjects: Subject[]): string {
    const rows = subjects
        .map(
            (s, i) => `
            <tr>
              <td class="num">${i + 1}</td>
              <td>${escapeHtml(s.code)}</td>
              <td>${escapeHtml(s.title)}</td>
              <td class="num">${formatUnits(s.units)}</td>
              <td class="num">${formatGrade(s.grade)}</td>
            </tr>`,
        )
        .join("");

    return `
        <table>
          <thead>
            <tr>
              <th class="num">#</th>
              <th>Code</th>
              <th>Subject</th>
              <th class="num">Units</th>
              <th class="num">Grade</th>
            </tr>
          </thead>
          <tbody>${rows}</tbody>
        </table>`;
}

function honorPill(honor: string | null): string {
    return honor ? `<span class="honor">${escapeHtml(honor)}</span>` : "";
}

const DISCLAIMER = (appName: string) => `
    <div class="disclaimer">
      Generated by ${escapeHtml(appName)} on ${formatToday()} from data entered
      by the user. This document is for personal reference only — it is
      <strong>not</strong> an official academic record and is not issued,
      certified, or endorsed by any school, registrar, or other authority.
    </div>`;

function metaBlock(rows: Array<[string, string] | null>): string {
    const items = rows
        .filter((r): r is [string, string] => r !== null)
        .map(([label, value]) => `<div><span>${label}:</span> ${value}</div>`)
        .join("");
    return items ? `<div class="meta">${items}</div>` : "";
}

