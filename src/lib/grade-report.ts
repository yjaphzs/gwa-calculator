function renderDocument(title: string, body: string): string {
    return `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1" />
    <title>${escapeHtml(title)}</title>
    <style>${STYLES}</style>
  </head>
  <body>
    <div class="doc">${body}</div>
  </body>
</html>`;
}

// ── Printing ─────────────────────────────────────────────────────────────────
/**
 * Writes a self-contained HTML document into a hidden iframe and opens the
 * browser print dialog for it. The iframe is removed after printing (with a
 * long fallback so it never leaks if `afterprint` doesn't fire).
 */
function printHtmlDocument(html: string): void {
    if (typeof window === "undefined") return;

    const iframe = document.createElement("iframe");
    iframe.setAttribute("aria-hidden", "true");
    iframe.style.position = "fixed";
    iframe.style.right = "0";
    iframe.style.bottom = "0";
    iframe.style.width = "0";
    iframe.style.height = "0";
    iframe.style.border = "0";
    document.body.appendChild(iframe);

    const frameWin = iframe.contentWindow;
    const frameDoc = frameWin?.document;
    if (!frameWin || !frameDoc) {
        iframe.remove();
        return;
    }

    let cleaned = false;
    const cleanup = () => {
        if (cleaned) return;
        cleaned = true;
        // Defer slightly so we never tear down while the dialog is mid-open.
        window.setTimeout(() => {
            if (document.body.contains(iframe)) document.body.removeChild(iframe);
        }, 500);
    };

    frameWin.addEventListener("afterprint", cleanup);

    frameDoc.open();
    frameDoc.write(html);
    frameDoc.close();

    // Let the iframe lay out before printing.
    window.setTimeout(() => {
        try {
            frameWin.focus();
            frameWin.print();
        } catch (err) {
            console.error("[grade-report] print failed:", err);
            cleanup();
        }
    }, 100);

    // Hard fallback so the iframe is never leaked if `afterprint` never fires.
    window.setTimeout(cleanup, 60000);
}

// ── Public API ───────────────────────────────────────────────────────────────
export interface SemesterReportOptions {
    appName: string;
    studentName?: string | null;
    /** e.g. "First Semester, S.Y. 2025-2026". Omit for the current working set. */
    periodLabel?: string | null;
    subjects: Subject[];
}

/** Prints a one-semester report for the given subjects. */
export function printSemesterReport({
    appName,
    studentName,
    periodLabel,
    subjects,
}: SemesterReportOptions): void {
    const { gwa, totalUnits } = computeGwa(subjects);
    const honor =
        gwa !== null && totalUnits >= HONOR_MIN_UNITS ? getAcademicHonor(gwa) : null;

    const body = `
        <div class="doc-header">
          <p class="app-name">${escapeHtml(appName)}</p>
          <h1 class="doc-title">Semester Report</h1>
          ${metaBlock([
              periodLabel ? ["Period", escapeHtml(periodLabel)] : ["Period", "Current (unsaved) subjects"],
              studentName ? ["Name", escapeHtml(studentName)] : null,
          ])}
        </div>
        ${subjectsTable(subjects)}
        <div class="summary">
          <div><span>Total units:</span> <strong>${formatUnits(totalUnits)}</strong></div>
          <div><span>GWA:</span> <strong>${formatGwa(gwa)}</strong></div>
          ${honorPill(honor)}
        </div>
        ${DISCLAIMER(appName)}`;

    const title = periodLabel ? `Semester Report — ${periodLabel}` : "Semester Report";
    printHtmlDocument(renderDocument(title, body));
}

export interface AcademicSummaryOptions {
    appName: string;
    studentName?: string | null;
    semesters: Semester[];
}

/** Prints a summary of all saved semesters, with a cumulative GWA. */
export function printAcademicSummary({
    appName,
    studentName,
    semesters,
}: AcademicSummaryOptions): void {
    const ordered = sortChronologically(semesters);

    const sections = ordered
        .map((sem) => {
            const { gwa, totalUnits } = computeGwa(sem.subjects);
            const honor =
                gwa !== null && totalUnits >= HONOR_MIN_UNITS
                    ? getAcademicHonor(gwa)
                    : null;
            const label = `${escapeHtml(sem.semester)} Semester, S.Y. ${escapeHtml(sem.schoolYear)}`;
            return `
                <div class="section">
                  <h2 class="section-title">${label}</h2>
                  ${subjectsTable(sem.subjects)}
                  <div class="summary">
                    <div><span>Units:</span> <strong>${formatUnits(totalUnits)}</strong></div>
                    <div><span>GWA:</span> <strong>${formatGwa(gwa)}</strong></div>
                    ${honorPill(honor)}
                  </div>
                </div>`;
        })
        .join("");

    const allSubjects = ordered.flatMap((s) => s.subjects);
    const { gwa: overallGwa, totalUnits: overallUnits } = computeGwa(allSubjects);
    // Latin (graduation) honors for the cumulative standing — distinct from the
    // per-semester scholarship honors above.
    const overallHonor =
        overallGwa !== null && overallUnits >= HONOR_MIN_UNITS
            ? getLatinHonor(overallGwa)
            : null;

    const body = `
        <div class="doc-header">
          <p class="app-name">${escapeHtml(appName)}</p>
          <h1 class="doc-title">Academic Summary</h1>
          ${metaBlock([
              studentName ? ["Name", escapeHtml(studentName)] : null,
              ["Semesters", String(ordered.length)],
          ])}
        </div>
        ${sections}
        <div class="overall">
          <h2 class="section-title">Overall</h2>
          <div class="summary">
            <div><span>Total units:</span> <strong>${formatUnits(overallUnits)}</strong></div>
            <div><span>Cumulative GWA:</span> <strong>${formatGwa(overallGwa)}</strong></div>
            ${honorPill(overallHonor)}
          </div>
        </div>
        ${DISCLAIMER(appName)}`;

    printHtmlDocument(renderDocument("Academic Summary", body));
}
